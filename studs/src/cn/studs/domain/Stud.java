package cn.studs.domain;

public class Stud {
	private String id;
	private String name;
	private String addr;
	private String sex;
	private Integer age;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	@Override
	public String toString() {
		return "Stud [id=" + id + ", name=" + name + ", addr=" + addr + ", sex=" + sex + ", age=" + age + "]";
	}
	
	
	

}
